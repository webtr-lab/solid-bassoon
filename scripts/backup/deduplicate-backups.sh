# Read backup checksums from index
read_backup_checksums() {
    python3 << 'PYEOF'
import json
import os

index_file = os.environ.get('INDEX_FILE', '')
if not index_file or not os.path.exists(index_file):
    exit(1)

try:
    with open(index_file, 'r') as f:
        index = json.load(f)

    # Create checksum -> backup mapping
    checksums = {}
    for backup in index['backups']:
        checksum = backup.get('checksum_sha256', '')
        backup_file = backup.get('backup_file', '')
        relative_path = backup.get('relative_path', '')

        if checksum and backup_file:
            if checksum not in checksums:
                checksums[checksum] = []
            checksums[checksum].append({
                'backup_file': backup_file,
                'relative_path': relative_path,
                'full_path': os.path.join(os.environ.get('BACKUP_ROOT', ''), relative_path) if relative_path else ''
            })

    # Output duplicate groups (checksums with more than one backup)
    for checksum, backups in checksums.items():
        if len(backups) > 1:
            print(f"DUPLICATE:{checksum}:{len(backups)}")
            for backup in backups:
                print(f"  {backup['full_path']}")

except Exception as e:
    print(f"Error reading backup index: {e}", file=__import__('sys').stderr)
    exit(1)
PYEOF
}

# Deduplicate backups using hard links
deduplicate_backups() {
    log_info "=========================================="
    log_info "Backup Deduplication"
    log_info "=========================================="

    local duplicates_found=0
    local hardlinks_created=0
    local space_saved=0

    # Read backup checksums
    local checksum_output=$(read_backup_checksums)

    if [ -z "$checksum_output" ]; then
        log_info "No duplicate backups detected"
        return 0
    fi

    # Process duplicate groups
    echo "$checksum_output" | while IFS=: read -r type checksum count rest; do
        if [ "$type" != "DUPLICATE" ]; then
            continue
        fi

        ((duplicates_found++))
        log_info "Found $count identical backups (checksum: ${checksum:0:16}...)"

        # Get the primary file (first one, by timestamp)
        local primary_file=""
        echo "$checksum_output" | grep -A $((count)) "^DUPLICATE:${checksum}:" | tail -n +2 | sort | head -1 | xargs -I {} basename {} | while read primary; do
            primary_file="${BACKUP_ROOT}/${primary}"

            # Create hard links for other identical backups
            echo "$checksum_output" | grep -A $((count)) "^DUPLICATE:${checksum}:" | tail -n +2 | sort | tail -n +2 | while read dup_file; do
                dup_path="${BACKUP_ROOT}/${dup_file}"

                if [ "$dup_file" = "$primary" ]; then
                    continue
                fi

                # Check if already a hard link to the same inode
                if [ -f "$dup_path" ] && [ -f "$primary_file" ]; then
                    primary_inode=$(stat -c %i "$primary_file" 2>/dev/null || stat -f %i "$primary_file")
                    dup_inode=$(stat -c %i "$dup_path" 2>/dev/null || stat -f %i "$dup_path")

                    if [ "$primary_inode" = "$dup_inode" ]; then
                        log_info "  ⊗ Already linked: $(basename $dup_path)"
                        continue
                    fi

                    # Get size before deduplication
                    local size_before=$(stat -c%s "$dup_path" 2>/dev/null || stat -f%z "$dup_path")

                    # Create hard link by removing duplicate and creating link
                    if rm -f "$dup_path" && ln "$primary_file" "$dup_path"; then
                        ((hardlinks_created++))
                        space_saved=$((space_saved + size_before))
                        log_info "  ✓ Hard linked: $(basename $dup_path)"
                    else
                        log_warn "  ⚠ Failed to create hard link for $dup_path"
                    fi
                fi
            done
        done
    done

    log_info "=========================================="
    log_info "Deduplication Summary"
    log_info "=========================================="
    log_info "Duplicate groups found: ${duplicates_found}"
    log_info "Hard links created: ${hardlinks_created}"

    if [ $hardlinks_created -gt 0 ]; then
        local space_saved_human=$(numfmt --to=iec-i --suffix=B "${space_saved}" 2>/dev/null || echo "${space_saved} bytes")
        log_info "Storage space saved: ${space_saved_human}"

        # Send success notification
        local email_details="Deduplication completed successfully.

Duplicates Found: ${duplicates_found}
Hard Links Created: ${hardlinks_created}
Storage Saved: ${space_saved_human}

Note: Hard links do not reduce disk usage but ensure that
identical backups share the same inode, improving efficiency."

        send_dedup_notification "SUCCESS" "${email_details}"
    else
        log_info "No deduplication opportunities found"
        send_dedup_notification "NO ACTION NEEDED" "All backups are unique or already deduplicated."
    fi
}

# Initialize log directory
mkdir -p "${LOG_DIR}"

# Run deduplication
log_info "Deduplication job started"
deduplicate_backups
exit_code=$?

log_info "Deduplication job completed (exit code: ${exit_code})"
exit $exit_code
