#!/bin/bash
#
# Backup Disk Usage Monitor
# Tracks backup directory size and sends alerts when thresholds are exceeded
#
# Features:
#   - Monitors backup directory size growth
#   - Tracks file count and compression statistics
#   - Alerts when approaching storage limits
#   - Stores historical usage data
#   - Predicts storage exhaustion based on growth rate
#   - Logs all monitoring data
#

# Note: Not using 'set -e' to allow script to continue even if JSON update fails
# Critical operations will explicitly check exit codes

# Configuration
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
BASE_DIR="$(dirname "$(dirname "${SCRIPT_DIR}")")"
BACKUP_ROOT="${BASE_DIR}/backups"
LOG_DIR="${BASE_DIR}/logs"
MONITOR_LOG="${LOG_DIR}/backup-disk-monitor.log"
HISTORY_FILE="${BACKUP_ROOT}/monitor-history.json"

# Monitoring thresholds (in percentage)
WARN_THRESHOLD=75      # Alert when 75% full
CRITICAL_THRESHOLD=90  # Critical alert when 90% full

# Load .env if it exists for environment variables
if [ -f "${BASE_DIR}/.env" ]; then
    set +a
    source "${BASE_DIR}/.env"
    set -a
fi

# Email settings
EMAIL_ENABLED="${BACKUP_EMAIL_ENABLED:-true}"
EMAIL_RECIPIENT="${BACKUP_EMAIL:-admin@example.com}"
EMAIL_SUBJECT_PREFIX="[Maps Tracker Backup Disk Monitor]"

# Colors
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m'

# Logging functions
log() {
    local level=$1
    shift
    local message="$@"
    local timestamp=$(date '+%Y-%m-%d %H:%M:%S')
    echo "[${timestamp}] [${level}] ${message}" | tee -a "${MONITOR_LOG}"
}

log_info() {
    log "INFO" "$@"
    echo -e "${GREEN}[INFO]${NC} $@"
}

log_warn() {
    log "WARN" "$@"
    echo -e "${YELLOW}[WARN]${NC} $@"
}

log_error() {
    log "ERROR" "$@"
    echo -e "${RED}[ERROR]${NC} $@" >&2
}

# Send email notification
send_disk_notification() {
    local status=$1
    local details=$2

    if [ "$EMAIL_ENABLED" != "true" ]; then
        return 0
    fi

    local subject="${EMAIL_SUBJECT_PREFIX} ${status^^} - Action Required"

    local email_body="════════════════════════════════════════════════════════════════
DISK USAGE ALERT - Maps Tracker Backup System
════════════════════════════════════════════════════════════════

Application:     Maps Tracker (Vehicle Tracking System)
Company:         [YOUR COMPANY NAME]
Server:          $(hostname)
Environment:     Production
Alert Type:      Backup Storage Capacity Monitoring
Status:          ${status^^}
Timestamp:       $(date '+%Y-%m-%d %H:%M:%S')

WHAT THIS MEANS:
──────────────────────────────────────────────────────────────────
The backup storage partition is approaching or has exceeded safe
usage thresholds. If disk fills completely, new backups cannot be
created and database recovery capability will be lost.

STORAGE ALERT THRESHOLDS:
──────────────────────────────────────────────────────────────────
• WARNING:  75% full - Start monitoring/cleanup
• CRITICAL: 90% full - Immediate action required

CURRENT DISK STATUS:
──────────────────────────────────────────────────────────────────
${details}

IMMEDIATE ACTION REQUIRED (in order of priority):
──────────────────────────────────────────────────────────────────
1. Log into server $(hostname)

2. Check disk space immediately:
   df -h ${BACKUP_ROOT}

3. Review backup files:
   du -sh ${BACKUP_ROOT}/*

4. Manually compress older backups NOW:
   ${BASE_DIR}/scripts/backup/archive-old-backups.sh

